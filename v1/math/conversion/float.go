// Convert a value to a 64-bit floating-point value.
//
//nolint:cyclop,varnamelen
func ToFloat64(val any) (float64, bool) {
	switch v := val.(type) {
	case float64:
		return v, true

	case float32:
		return float64(v), true

	case int:
		return float64(v), true
	case int8:
		return float64(v), true
	case int16:
		return float64(v), true
	case int32:
		return float64(v), true
	case int64:
		return float64(v), true

	case uint:
		return float64(v), true
	case uint8:
		return float64(v), true
	case uint16:
		return float64(v), true
	case uint32:
		return float64(v), true
	case uint64:
		return float64(v), true

	default:
		return 0, false
	}
}

